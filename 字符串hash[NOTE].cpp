/*
设串长为N,字符集大小为M,默认串为s[1..N].
1.取一个大于M的素数P.
2.预处理出P^0,P^1..P^N(记作p[i]).
3.计算前缀hash.
hsh[1]=s[1]*P^0.
hsh[2]=s[1]*P^1+s[2]*P^0.
hsh[3]=s[1]*P^2+s[2]*P^1+s[3]*P^0.
……
即hsh[i]=hsh[i-1]*P+s[i];
//hsh[i-j]=hsh[j]-hsh[i-1]*p^(j-i+1)
如果两个子串的hash值相等,那么我们就认为两个子串相等.


如果hash乘出来的数过大怎么办?
----取模.（大质数）
----自然溢出.
如果不想让它发生冲突怎么办?
----hash表.
----双关键字hash.


*/ 
